let nomes = ['Thiago', 'Maria', 'José']
console.log(nomes)
// Array com 3 elementos guardados

nomes[0] = 'Augusto'
console.log(nomes)
//A posição zero (Thiago) será substituida pelo valor (Augusto)

console.log(nomes.push = 'Thiago')
//Adiconará o valor (Thiago) no final.

console.log(nomes.length)
//Verifica quantidade de elementos no Array

console.log(nomes.pop())
//Retira o último elemento

console.log(nomes.shift())
//Retira o Primeiro Elemento

console.log(nomes.indexOf('Augusto'))
//Procura o valor e retorna